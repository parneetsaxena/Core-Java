package problems;

public class ArithmeticOperations {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int result = a+b;
        System.out.println("The sum of "+a+" and "+b+" is: "+result);
        result = a-b;
        System.out.println("The difference of "+a+" and "+b+" is: "+result);
        result = a*b;
        System.out.println("The product of "+a+" and "+b+" is: "+result);
        result = b/a;
        System.out.println("The quotient of "+b+" divided by "+a+" is: "+result);
        result = a%b;
        System.out.println("The remainder of "+a+" by "+b+" is: "+result);

    }
}
